#include <stdio.h>
#include <stdlib.h>

int main() {
    int nYear, nMonth, nDay;
    char name[20];

    // 날짜 입력 받기
    printf("[현재 날짜를 'yyyy-mm-dd' 형식으로 입력하세요]: ");
    scanf("%d-%d-%d", &nYear, &nMonth, &nDay);  // 주소 전달

    // 이름 입력 받기
    printf("[당신의 이름을 입력하세요]: ");
    scanf("%s", name);
    
    printf("**입력이 정상적으로 처리되었습니다~**\n");

// 3초 대기 (OS에 따라 명령어 변경 필요)
#ifdef _WIN32
    system("timeout /t 3 >nul");  // Windows용
    system("cls");  // 화면 지우기
#else
    system("sleep 3");  // Linux/macOS용
    system("clear");  // 화면 지우기
#endif

    printf("\n");  // 올바른 줄 바꿈

    // 삼각형 출력
    printf("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
    for (int i = 1; i <= 6; i++) {
        for (int j = 0; j < i; j++) {
            printf("*");
        }
        for (int j = 0; j < 73; j++) {  // 공백을 48칸으로 조정
            printf(" ");
        }
        for (int j = 0; j < (7 - i); j++) {  // 오른쪽 삼각형 출력
            printf("*");
        }
        printf("\n");
    }

    // 마그라테아 관련 텍스트 출력
    printf("*  <~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~>  ******\n");
    printf("**                        [마그라테아 ver 0.1]                             *****\n");
    printf("***  풀 한 포기 없는 황무지에서 반짝이는 행성을 만들내는 곳 마그라테아,     ****\n");
    printf("****  사람들이 보지 못하는 잠재력을 찾고 전문가의 손길을 더해 보석을 빗는 곳,***\n");
    printf("*****   마그라테아에 오신걸 환영합니다~                                       **\n");
    printf("******  <~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~>  *\n");
    printf("\n");

    // 아래 정보 출력
    printf("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
    printf("[사용자]: %s                                           ", name);
    printf("[실행 시간]: %04d-%02d-%02d\n", nYear, nMonth, nDay);
    printf("==================================================================================\n");
    
    return 0;
}
//실행 방법 : 처음 입력 ex)2018-01-09, 두번째 입력 ex)홍길동
